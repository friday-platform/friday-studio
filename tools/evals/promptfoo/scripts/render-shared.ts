/**
 * Shared types + helpers for suite render.ts scripts.
 *
 * Each suite under `suites/<name>/render.ts` defines its own `Case[]`,
 * converts cases to `PromptfooTest[]` (calling the real Friday prompt-builder
 * once per case), and writes the result via `writeGeneratedTests`. Keeps the
 * header comment + output path consistent across suites.
 */

import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { stringify } from "@std/yaml";

/** Shape promptfoo expects in a `tests.yaml` array entry. */
export interface PromptfooTest {
  description: string;
  vars: Record<string, unknown>;
  assert: Array<Record<string, unknown>>;
}

/**
 * Serialize `tests` to `tests.generated.yaml` next to the caller's render.ts.
 *
 * @param importMetaUrl  Caller's `import.meta.url` (locates the output dir).
 * @param sourceLabel    Human-readable name of the function the renderer wraps
 *                       — appears in the generated file's header so a reader
 *                       can trace YAML back to its source.
 */
export async function writeGeneratedTests(
  importMetaUrl: string,
  sourceLabel: string,
  tests: PromptfooTest[],
): Promise<void> {
  const yaml = stringify(tests, { lineWidth: 100 });
  const outPath = join(dirname(fileURLToPath(importMetaUrl)), "tests.generated.yaml");
  const header = `# AUTO-GENERATED by render.ts. Do NOT edit by hand.
# Source: render.ts (which calls ${sourceLabel}).
# Regenerate with: deno task evals:render-promptfoo

`;
  await Deno.writeTextFile(outPath, header + yaml);
  console.log(`wrote ${tests.length} tests → ${outPath}`);
}
