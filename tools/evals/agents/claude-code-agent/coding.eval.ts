import { claudeCodeAgent } from "@atlas/bundled-agents";
import { client, parseResult } from "@atlas/client/v2";
import { assert, assertEquals } from "@std/assert";
import * as path from "@std/path";
import { AgentContextAdapter } from "../../lib/context.ts";
import { llmJudge } from "../../lib/llm-judge.ts";
import { loadCredentials } from "../../lib/load-credentials.ts";
import { saveSnapshot } from "../../lib/snapshot.ts";

/** Check if file exists at given path. */
async function fileExists(filePath: string): Promise<boolean> {
  try {
    await Deno.stat(filePath);
    return true;
  } catch {
    return false;
  }
}

Deno.test({
  name: "Claude Code Agent: Filesystem Operations - Read and Write",
  sanitizeOps: false,
  sanitizeResources: false,
  async fn(t) {
    await loadCredentials();
    const tempWorkspace = await Deno.makeTempDir({ prefix: "claude-code-eval-" });

    try {
      // Setup test files in workspace
      const testFileName = "test-input.txt";
      const testFilePath = path.join(tempWorkspace, testFileName);
      const testContent = "Hello from the test file!\nThis is line 2.\nThis is line 3.";
      await Deno.writeTextFile(testFilePath, testContent);

      const packageJsonPath = path.join(tempWorkspace, "package.json");
      const packageJsonContent = JSON.stringify(
        {
          name: "test-workspace",
          version: "1.0.0",
          description: "A test workspace for Claude Code agent eval",
        },
        null,
        2,
      );
      await Deno.writeTextFile(packageJsonPath, packageJsonContent);

      const adapter = new AgentContextAdapter({}, {});
      adapter.enableTelemetry();

      await t.step("Read existing file", async () => {
        adapter.reset();
        const context = adapter.createContext({ telemetry: true });

        const result = await claudeCodeAgent.execute(
          `Read the file at absolute path "${testFilePath}" and tell me how many lines it contains.`,
          context,
        );

        const metrics = adapter.getMetrics();
        const streamEvents = adapter.getStreamEvents();

        assert(result.ok, "Agent executed successfully");
        assert(result.data.response.length > 0, "Response should not be empty");
        assert(
          result.data.response.includes("3") ||
            result.data.response.toLowerCase().includes("three"),
          "Response should mention 3 lines",
        );

        await saveSnapshot({
          testPath: new URL(import.meta.url),
          data: { testName: "Read existing file", result, metrics, streamEvents },
          pass: true,
        });
      });

      const newFileName = "agent-output.txt";
      const newFilePath = path.join(tempWorkspace, newFileName);

      await t.step("Write new file", async () => {
        adapter.reset();
        const context = adapter.createContext({ telemetry: true });

        const result = await claudeCodeAgent.execute(
          `Create a new file at absolute path "${newFilePath}" with the content "Generated by Claude Code agent" on the first line and "Testing write operations" on the second line.`,
          context,
        );

        const metrics = adapter.getMetrics();
        const streamEvents = adapter.getStreamEvents();

        // Verify file was created
        assert(await fileExists(newFilePath), "New file should exist in workspace");

        // Verify content
        const writtenContent = await Deno.readTextFile(newFilePath);
        assert(
          writtenContent.includes("Generated by Claude Code agent"),
          "File should contain expected content",
        );
        assert(
          writtenContent.includes("Testing write operations"),
          "File should contain second line",
        );

        await saveSnapshot({
          testPath: new URL(import.meta.url),
          data: {
            testName: "Write new file",
            result,
            metrics,
            streamEvents,
            fileCreated: newFilePath,
            fileContent: writtenContent,
          },
          pass: true,
        });
      });

      await t.step("Read and modify existing file", async () => {
        adapter.reset();
        const context = adapter.createContext({ telemetry: true });

        const result = await claudeCodeAgent.execute(
          `Read the file at absolute path "${packageJsonPath}", add a new field called "author" with the value "Claude Code Test", and write the updated content back to the same file.`,
          context,
        );

        const metrics = adapter.getMetrics();
        const streamEvents = adapter.getStreamEvents();

        const modifiedContent = await Deno.readTextFile(packageJsonPath);
        const modifiedJson = JSON.parse(modifiedContent) as { author?: string };
        assertEquals(
          modifiedJson.author,
          "Claude Code Test",
          "package.json should have new author field",
        );

        await saveSnapshot({
          testPath: new URL(import.meta.url),
          data: {
            testName: "Read and modify existing file",
            result,
            metrics,
            streamEvents,
            modifiedContent: modifiedJson,
          },
          pass: true,
        });
      });

      await t.step("Verify absolute path file creation", async () => {
        adapter.reset();
        const context = adapter.createContext({ telemetry: true });

        const testFile = "workspace-test.txt";
        const expectedPath = path.join(tempWorkspace, testFile);

        const result = await claudeCodeAgent.execute(
          `Create a file at absolute path "${expectedPath}" with the text "workspace boundary test"`,
          context,
        );

        const metrics = adapter.getMetrics();
        const streamEvents = adapter.getStreamEvents();

        assert(await fileExists(expectedPath), `File should exist at ${expectedPath}`);
        const fileContent = await Deno.readTextFile(expectedPath);
        assert(
          fileContent.includes("workspace boundary test"),
          "File should contain expected text",
        );

        await saveSnapshot({
          testPath: new URL(import.meta.url),
          data: {
            testName: "Verify absolute path file creation",
            result,
            metrics,
            streamEvents,
            filePath: expectedPath,
            fileContent,
          },
          pass: true,
        });
      });

      await t.step("Create artifact for code analysis", async () => {
        adapter.reset();
        const context = adapter.createContext({ telemetry: true });

        const result = await claudeCodeAgent.execute(
          `Analyze the files in "${tempWorkspace}". Include: 1) List of files found, 2) Summary of what each file contains, 3) Overall assessment of the workspace contents.`,
          context,
        );

        const metrics = adapter.getMetrics();
        const streamEvents = adapter.getStreamEvents();

        assert(result.ok, "Agent executed successfully");
        const artifactRef = result.data.artifactRef;

        const artifactId = artifactRef.id;
        const artifactResponse = await parseResult(
          client.artifactsStorage[":id"].$get({ param: { id: artifactId } }),
        );

        assert(
          artifactResponse.ok,
          `Failed to fetch artifact: ${JSON.stringify(!artifactResponse.ok && artifactResponse.error)}`,
        );

        const artifact = artifactResponse.data.artifact;
        assert(artifact, "Artifact should exist");
        assert(artifact.data, "Artifact should have data");

        // Verify artifact quality with LLM judge
        const artifactEval = await llmJudge({
          criteria: `
            The artifact should contain:
            1. A list of files found in the workspace (test-input.txt, package.json, agent-output.txt, workspace-test.txt)
            2. A summary of each file's contents
            3. An overall assessment or analysis of the workspace
            4. Clear, organized presentation of the information
          `,
          agentOutput: JSON.stringify(artifact.data),
        });

        assert(artifactEval.pass, `Artifact quality check failed: ${artifactEval.justification}`);

        await saveSnapshot({
          testPath: new URL(import.meta.url),
          data: {
            testName: "Create artifact for code analysis",
            result,
            artifact,
            metrics,
            streamEvents,
            evaluation: artifactEval,
          },
          pass: true,
        });
      });

      await t.step("Overall quality check", async () => {
        adapter.reset();
        const context = adapter.createContext({ telemetry: true });

        const finalResult = await claudeCodeAgent.execute(
          `List all files in the directory at absolute path "${tempWorkspace}" and provide a brief summary of what you find.`,
          context,
        );

        const metrics = adapter.getMetrics();
        const streamEvents = adapter.getStreamEvents();

        const response = finalResult;
        assert(finalResult.ok, "Agent executed successfully");
        const evaluation = await llmJudge({
          criteria: `
            The agent should:
            1. Successfully list files in the directory
            2. Mention the files we created (test-input.txt, package.json, agent-output.txt, workspace-test.txt)
            3. Provide clear, concise information without excessive technical details
            4. Demonstrate it can read and understand file contents
          `,
          agentOutput: response,
        });

        assert(evaluation.pass, `LLM judge failed: ${evaluation.justification}`);

        await saveSnapshot({
          testPath: new URL(import.meta.url),
          data: {
            testName: "Overall quality check",
            tempWorkspace,
            filesCreated: [testFileName, "package.json", newFileName, "workspace-test.txt"],
            finalResult,
            metrics,
            streamEvents,
            evaluation,
          },
          pass: evaluation.pass,
        });
      });
    } finally {
      await Deno.remove(tempWorkspace, { recursive: true });
    }
  },
});
